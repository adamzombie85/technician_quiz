import json
import os

def apply_enrichment(results_json_str):
    output_file = "unified_database_enriched.json"
    
    if not os.path.exists(output_file):
        print(f"❌ 找不到檔案: {output_file}")
        return

    with open(output_file, "r", encoding="utf-8") as f:
        data = json.load(f)

    results = json.loads(results_json_str)
    count = 0
    for res in results:
        idx = res.get("idx")
        if idx is not None and idx < len(data):
            data[idx]["keyword_tag"] = res.get("kw", "")
            data[idx]["knowledge_tag"] = res.get("kn", "")
            data[idx]["explanation"] = res.get("ex", "")
            count += 1
    
    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"✅ 成功更新 {count} 題資料。")

